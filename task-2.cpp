#include <iostream>
#include <cstdlib>
#include <ctime>
#include <chrono>

using namespace std;

int main() {
    // задаем размер массива - миллион элементов
    const int n = 1000000;
    
    // выделяем динамически память под массив из миллиона чисел
    int* arr = new int[n];
    
    // инициализируем генератор случайных чисел текущим временем
    srand(time(0));
    
    // заполняем массив случайными числами от 1 до 100
    for (int i = 0; i < n; i++) {
        // генерируем случайное число от 1 до 100 и записываем в массив
        arr[i] = rand() % 100 + 1;
    }
    
    // запоминаем время начала выполнения алгоритма
    auto start = chrono::high_resolution_clock::now();
    
    // инициализируем переменные для минимума и максимума первым элементом массива
    int min_val = arr[0];
    int max_val = arr[0];
    
    // проходим по всем элементам массива начиная со второго
    for (int i = 1; i < n; i++) {
        // если текущий элемент меньше найденного минимума
        if (arr[i] < min_val) {
            // обновляем минимум
            min_val = arr[i];
        }
        // если текущий элемент больше найденного максимума
        if (arr[i] > max_val) {
            // обновляем максимум
            max_val = arr[i];
        }
    }
    
    // запоминаем время окончания выполнения алгоритма
    auto end = chrono::high_resolution_clock::now();
    
    // вычисляем длительность выполнения в микросекундах
    auto duration = chrono::duration_cast<chrono::microseconds>(end - start);
    
    // выводим результаты на экран
    cout << "Последовательный поиск:" << endl;
    cout << "Минимальный элемент: " << min_val << endl;
    cout << "Максимальный элемент: " << max_val << endl;
    cout << "Время выполнения: " << duration.count() << " микросекунд" << endl;
    
    // освобождаем выделенную память
    delete[] arr;
    
    return 0;
}